package com.siteview.workorder.odata.yft.entities;

/**
 * odata接口初始对象，包含id和name
 * 
 * @author Administrator
 *
 */
public class OdataObject {
	private String id;
	private String name;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
