package com.siteview.workorder.odata.yft.entities;

public class FaultType {
	private String faultTypeName;
	private String faultTypeRecId;
	public String getFaultTypeName() {
		return faultTypeName;
	}
	public void setFaultTypeName(String faultTypeName) {
		this.faultTypeName = faultTypeName;
	}
	public String getFaultTypeRecId() {
		return faultTypeRecId;
	}
	public void setFaultTypeRecId(String faultTypeRecId) {
		this.faultTypeRecId = faultTypeRecId;
	}
}
